        private TransaccionesEN InformacionDelaTransaccion(LocacionEN oLocacion, String TipoDeOperacion, String Descripcion, String Estado)
        {
            TransaccionesEN oRegistroEN = new TransaccionesEN();

            oRegistroEN.idregistro = oLocacion.idLocacion;
            oRegistroEN.Modelo = "General";
            oRegistroEN.Modulo = "LocacionAD";
            oRegistroEN.Tabla = "Locacion";
            oRegistroEN.tipodeoperacion = TipoDeOperacion;
            oRegistroEN.Estado = Estado;
            oRegistroEN.ip = oLocacion.oLoginEN.NumeroIP;
            oRegistroEN.idusuario = oLocacion.oLoginEN.idUsuario;
            oRegistroEN.idusuarioaprueba = oLocacion.oLoginEN.idUsuario;
            oRegistroEN.descripciondelusuario = DescripcionDeOperacion;
            oRegistroEN.descripcioninterna = Descripcion;
            oRegistroEN.NombreDelEquipo = oLocacion.oLoginEN.NombreDelComputador;

            return oRegistroEN;
        }
        
        private string InformacionDelRegistro(LocacionEN oRegistroEN) {
            string Cadena = @"idLocacion: {0}, Nombre: {1}, Descripcion: {2}, idUsuarioDeCreacion: {3}, FechaDeCreacion: {4}, idUsuarioModificacion: {5}, FechaDeModificacion: {6}";
