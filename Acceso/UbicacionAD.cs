        private TransaccionesEN InformacionDelaTransaccion(UbicacionEN oUbicacion, String TipoDeOperacion, String Descripcion, String Estado)
        {
            TransaccionesEN oRegistroEN = new TransaccionesEN();

            oRegistroEN.idregistro = oUbicacion.idUbicacion;
            oRegistroEN.Modelo = "General";
            oRegistroEN.Modulo = "UbicacionAD";
            oRegistroEN.Tabla = "Ubicacion";
            oRegistroEN.tipodeoperacion = TipoDeOperacion;
            oRegistroEN.Estado = Estado;
            oRegistroEN.ip = oUbicacion.oLoginEN.NumeroIP;
            oRegistroEN.idusuario = oUbicacion.oLoginEN.idUsuario;
            oRegistroEN.idusuarioaprueba = oUbicacion.oLoginEN.idUsuario;
            oRegistroEN.descripciondelusuario = DescripcionDeOperacion;
            oRegistroEN.descripcioninterna = Descripcion;
            oRegistroEN.NombreDelEquipo = oUbicacion.oLoginEN.NombreDelComputador;

            return oRegistroEN;
        }
        
        private string InformacionDelRegistro(UbicacionEN oRegistroEN) {
            string Cadena = @"idUbicacion: {0}, Nombre: {1}, Descripcion: {2}, idUsuarioDeCreacion: {3}, FechaDeCreacion: {4}, idUsuarioModificacion: {5}, FechaDeModificacion: {6}, idTipoDeUbicacion: {7}";
            Cadena = string.Format(Cadena, oRegistroEN.idUbicacion, oRegistroEN.Nombre, oRegistroEN.Descripcion, oRegistroEN.idUsuarioDeCreacion, oRegistroEN.FechaDeCreacion, oRegistroEN.idUsuarioModificacion, oRegistroEN.FechaDeModificacion, oRegistroEN.oTipoDeUbicacionEN.idTipoDeUbicacion);
            Cadena = Cadena.Replace(",", Environment.NewLine);
            return Cadena;            
        }

